package Java;

public class Poly {

    public static int add(int i, int j) {
        return i + j;
    }

    public static int add(int i, int j, int k) {
        return i + j + k;
    }

    public static int add(int i, int j, int k,int l) {
        return i + j + k + l;

    }

    public static float add(int i, int j, float k) {
        return i + j + k;
    }

}